class UserDetails {
  final String name;
  final String sic;
  final String branch;
  final String email;
  final String year;
  final int? phoneNumber;
  final String imageUrl;

  UserDetails({
    required this.name,
    required this.sic,
    required this.branch,
    required this.email,
    required this.year,
    required this.phoneNumber,
    required this.imageUrl,
  });
}
